def bubble_sort(arr_list):
    """
    冒泡排序
    两两比较相邻元素的值，每趟排序都会确定一个元素的位置
    时间复杂度：最好O(n),最坏O(n^2),平均O(n^2)
    空间复杂度O(1)
    稳定
    """
    for i in range(len(arr_list) - 1):
        for j in range(len(arr_list) - 1 - i):
            if arr_list[j] > arr_list[j+1]:
                arr_list[j], arr_list[j+1] = arr_list[j+1], arr_list[j]
    return arr_list


def select_sort(arr_list):
    """
    选择排序
    直接遍历取出最小值与第一个数交换位置，依次循环，每趟都能确定一个元素的位置
    时间复杂度O(n^2)
    空间复杂度O(1)
    不稳定
    :param arr_list:
    :return:
    """
    for i in range(len(arr_list) - 1):
        # 默认最小值得index位置
        min_index = i
        for j in range(i, len(arr_list)):
            if arr_list[j] < arr_list[min_index]:
                min_index = j
        arr_list[i], arr_list[min_index] = arr_list[min_index], arr_list[i]
    return arr_list


def insert_sort(arr_list):
    """
    插入排序
    打扑克抓牌过程是典型的插入算法，将数依次插入到有序数列中，假设第一数为有序数列，经过n-1趟后得到有序数列
    时间复杂度：最好O(n),最坏O(n^2),平均O(n^2)
    空间复杂度O(1)
    稳定
    :param arr_list:
    :return:
    """
    for i in range(len(arr_list)-1):
        for j in range(i+1):
            if arr_list[i+1] < arr_list[i]:
                arr_list[i], arr_list[i+1] = arr_list[i+1], arr_list[i]
            i -= 1

    return arr_list


def shell_sort(arr_list):
    """
    希尔排序
    一种插入排序的改进版，比较一定间隔的元素，然后在缩小间隔继续比较，直到间隔为1也就是相邻的元素为止
    时间复杂度：最好O(nlog^2n),最坏O(n^2),依赖于间隔大小
    不稳定
    空间复杂度O(1)
    一般数量小的排序比较好，数量大了效率就不行了
    :param arr_list:
    :return:
    """
    arr_len = len(arr_list)
    d = arr_len // 2
    while d >= 1:
        for i in range(arr_len-d):
            if arr_list[i+d] < arr_list[i]:
                arr_list[i+d], arr_list[i] = arr_list[i], arr_list[i+d]
        d = d // 2
    return insert_sort(arr_list)


def quick_sort(arr_list):
    """
    快速排序
    是冒泡排序的一种改进，基本思想是分治法，随机取列表中的一个元素pivot为基准，一趟排序后将列表分为2部分，一部分大于pivot一部分小于pivot，
    然后分别递归两个部分列表，直到只有有个元素或为空
    时间复杂度：最好O(nlogn),最坏O(n^2),平均O(nlogn)
    空间复杂度：因为用递归最坏为大O(n)平均为O(logn)
    不稳定
    :param arr_list:
    :return:
    """
    lef = []  # 左边列表小于pivot
    rig = []  # 右边列表大于pivot
    mid = []  # 中间列表等于pivot， 也可以不用

    if len(arr_list) <= 1:
        return arr_list
    else:
        pivot = arr_list[0]
        for i in arr_list:
            if i < pivot:
                lef.append(i)
            elif i > pivot:
                rig.append(i)
            else:
                mid.append(i)
        lef = quick_sort(lef)
        rig = quick_sort(rig)
        return lef + mid + rig


def quick_sort2(arr_list):
    """
    写法不同
    :param arr_list:
    :return:
    """
    if len(arr_list) <= 1:
        return arr_list
    else:
        pivot = arr_list[0]
        lef = [x for x in arr_list if x <= pivot]
        rig = [x for x in arr_list if x > pivot]
        return quick_sort(lef) + [pivot] + quick_sort(rig)


def heap_sort(arr_list):
    arr_list_len = len(arr_list)

    for i in range((arr_list_len-1)//2, -1, -1):
        arr_list_len = arr_list_len - i
        while True:
            parent_root = i
            left_child = 2 * parent_root + 1
            right_child = left_child + 1

            if left_child > arr_list_len - 1:  # 左节点大于列表最后一个index
                break
            if right_child <= arr_list_len - 1 and arr_list[left_child] < arr_list[right_child]:  # 右节点小于等于列表最后一个index
                left_child += 1
            if arr_list[parent_root] < arr_list[left_child]:
                arr_list[parent_root], arr_list[left_child] = arr_list[left_child], arr_list[parent_root]
                parent_root = left_child  # 设置跳出点
            else:
                break

        arr_list[0], arr_list[arr_list_len-1] = arr_list[arr_list_len-1], arr_list[0]

    return arr_list


